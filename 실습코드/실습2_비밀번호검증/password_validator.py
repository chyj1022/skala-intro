import re

password = input("검증할 비밀번호를 입력하세요: ")

# 소문자, 대문자, 숫자, 기호를 각각 최소 1개 이상 포함하는지 확인하는 정규 표현식
pattern = re.compile(
    r'^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[!@#$%^&*()_+\-=\[\]{};:\'",.<>/?\\|`~]).+$'
)

if pattern.match(password):
    print("사용 가능한 비밀번호입니다.")
else:
    print("비밀번호는 영문 소문자, 대문자, 숫자, 기호를 각각 최소 1개 이상 포함해야 합니다.")
